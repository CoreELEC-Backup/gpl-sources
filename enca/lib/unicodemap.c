/**
 * EncaUnicodeMap:
 * @name: Charset name (any, Enca's is best).
 * @tstart: Table start, character codes less than tstart maps to themselves.
 * @map: The map itself.
 *
 * 8bit charset -> Unicode mapping.
 **/
